    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String searchString = TextSearch.getText();
        int Busnum =TextSearchBUS.getText().equals("")? 0 :Integer.parseInt(TextSearchBUS.getText());
        int Econum =TextSearchECO.getText().equals("")? 0 : Integer.parseInt(TextSearchECO.getText());
        System.out.println(searchString + " "+ Busnum + " "+ Econum);
        ShowModel(model, bus.Search(searchString, Busnum, Econum));

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int confirmation = JOptionPane.showConfirmDialog(this, "Quay lại menu?", "Xác nhận quay lại", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            MENU menu = new MENU();
            menu.setVisible(true);
            closeFrame(this);
        }
    }//GEN-LAST:event_jButton5ActionPerformed
    private void closeFrame(JFrame frame) {
        WindowEvent closeWindow = new WindowEvent(frame, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(closeWindow);
    }
