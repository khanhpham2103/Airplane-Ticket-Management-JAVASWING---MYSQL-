    private ArrayList<LoTrinhDTO> Search(String Search) {
        ArrayList<LoTrinhDTO> data = ltbs.getList();
        ArrayList<LoTrinhDTO> res = new ArrayList<>();
        String getCombox = (String) ComboxFilter.getModel().getSelectedItem();
        switch (getCombox) {
            case "Nơi Đến": {
                for (LoTrinhDTO a : data) {
                    if (a.getArrivalLocation().toLowerCase().indexOf(Search.toLowerCase()) != -1) {
                        res.add(a);
                    }
                }
                return res;
            }
            case "Nơi Đi": {
                for (LoTrinhDTO a : data) {
                    if (a.getDePartureLocation().toLowerCase().indexOf(Search.toLowerCase()) != -1) {
                        res.add(a);
                    }
                }
                return res;
            }
        }
        JOptionPane.showMessageDialog(null, "Thông Tin Không Tìm Thấy");
        
        return null;
    }

    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed
        if (TextFilter.getText().equals("") || ComboxFilter.getModel().getSelectedItem().equals("Không")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin cần tìm kiếm và chọn mục cần tìm \n");
        } else {
            ArrayList<LoTrinhDTO> res = Search(TextFilter.getText());
            ShowModel(model, res);
        }
    }//GEN-LAST:event_BtnSearchActionPerformed

