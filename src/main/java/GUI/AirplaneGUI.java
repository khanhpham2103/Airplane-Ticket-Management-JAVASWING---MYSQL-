    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Idairplane = TextMaMayBay.getText();
        String Name = TextTen.getText();
        String Brand = TextHang.getText();
        String MaHang = (String) ComboxAirline.getModel().getSelectedItem();
        if (MaHang.equals("Không")) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn Mã Tuyến Bay");
            return;
        }
        MaHang = MaHang.substring(0, MaHang.indexOf(' '));
        System.out.println(MaHang);
        int busNum = Integer.parseInt(TextBUSNum.getText());
        int eco = Integer.parseInt(TextECONum.getText());
        AirplaneDTO dto = new AirplaneDTO(Idairplane, Name, Brand, MaHang, eco, busNum);
        bus.add(dto);
        ShowModel(model, bus.getList());
    }//GEN-LAST:event_jButton1ActionPerformed

