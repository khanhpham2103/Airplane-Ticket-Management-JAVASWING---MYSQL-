    public void CreateHeader() {
        Vector header = new Vector();
        header.add("airplaneID");
        header.add("airplaneName");
        header.add("airplaneBrand");
        header.add("airlineID");
        header.add("ECO Seats");
        header.add("BUS Seats");
        if (model.getRowCount() == 0) {
            model = new DefaultTableModel(header, 0);
        }
        tbl.setModel(model);
    }

    public void CreateComboBox() {
        HHKhongBUS busHH = new HHKhongBUS();
        busHH.list();
        ComboxAirline.addItem("Không");
        ArrayList<HHKhongDTO> ds = busHH.getList();
        for (HHKhongDTO dto : ds) {
            ComboxAirline.addItem(dto.getAirlineID() + " - " + dto.getAirlineName());
        }
    }

    /**
     *
     * @param md
     * @param ds
     */
    public void ShowModel(DefaultTableModel md, ArrayList<AirplaneDTO> ds) {
        Vector data;
        model.setRowCount(0);
        for (AirplaneDTO a : ds) {
            data = new Vector();
            data.add(a.getAirplaneID());
            data.add(a.getAirplaneName());
            data.add(a.getAirplaneBrand());
            data.add(a.getAirlineID());
            data.add(String.valueOf(a.getEcoSeats()));
            data.add(String.valueOf(a.getBusSeats()));
            md.addRow(data);
        }
        tbl.setModel(md);
    }

    public void list() {
        if (bus.getList() == null) {
            bus.list();
        }
        ShowModel(model, bus.getList());
    }

