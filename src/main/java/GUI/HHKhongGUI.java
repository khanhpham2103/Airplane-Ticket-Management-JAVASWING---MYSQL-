    public void CreateHeader() {
        Vector header = new Vector();
        header.add("Mã hãng hàng không");
        header.add("Tên hãng hàng không");
        if (model.getRowCount() == 0) {
            model = new DefaultTableModel(header, 0);
        }
        tbl.setModel(model);
    }
    
    public void ShowModel(DefaultTableModel md, ArrayList<HHKhongDTO> hhkArrList) {
        Vector data;
        model.setRowCount(0);
        for (HHKhongDTO n : hhkArrList) {
            data = new Vector();
            data.add(n.getAirlineID());
            data.add(n.getAirlineName());
            md.addRow(data);
        }
        tbl.setModel(md);
    }
    
    public void list() {
        // Nếu mà có dữ liệu hiện tại thì dùng lun , không có thì kéo db lên
        if (bus.getList() == null) {
            bus.list();
        }
        ArrayList<HHKhongDTO> dsdto = bus.getList();
        ShowModel(model, dsdto);
    }
    
    private void search(){
        model.setRowCount(0);
        String ID = "";
        for(int i = 0; i < bus.getAirline().size(); i++){
            if(bus.getAirline().get(i).toLowerCase().contains(txtFind.getText().toLowerCase())){
                ID = bus.getAirline().get(i).substring(0,2);
                for (int j = 0; j < bus.getList().size(); j++) {
                    if(bus.getList().get(j).getAirlineID().equals(ID)){
                        HHKhongDTO temp = bus.getList().get(j);
                        Vector data = new Vector();
                        data.add(temp.getAirlineID());
                        data.add(temp.getAirlineName());
                        model.addRow(data);
                    }
                }
            }
        }
        if(model.getRowCount() == 0){
            searchWarning.setVisible(true);
            searchWarning.setText("Không có thông tin bạn cần tìm hoặc đã nhập sai");
        }
        else{
            searchWarning.setVisible(true);
            searchWarning.setText("Có "+model.getRowCount()+" kết quả");
            tbl.setModel(model);
        }
    }
