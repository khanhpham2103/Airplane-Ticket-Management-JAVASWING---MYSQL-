    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String airlineID = txtAirlineID.getText();
        String airlineName = txtName.getText();
        boolean check = false;
        ArrayList<HHKhongDTO> airlineArrList = bus.getList();
        for (HHKhongDTO hhk : airlineArrList) {
            if (airlineID.equals(hhk.getAirlineID())) {
                check = true;
                break;
            }
        }
        if (check == true) {
            JOptionPane.showMessageDialog(null, "Mã hãng hàng không đã tồn tại");
        } else {
            HHKhongDTO hhk = new HHKhongDTO(airlineID, airlineName);
            bus.add(hhk);
            ShowModel(model, bus.getList());
        }
    }//GEN-LAST:event_btnAddActionPerformed

