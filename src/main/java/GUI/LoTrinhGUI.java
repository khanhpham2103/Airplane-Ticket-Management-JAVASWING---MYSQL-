    public void ShowModel(DefaultTableModel md, ArrayList<LoTrinhDTO> dsdtp) {
        Vector data;
        model.setRowCount(0);
        for (LoTrinhDTO n : dsdtp) {
            
            data = new Vector();
            data.add(n.getRouteID());
            data.add(n.getDePartureLocation());
            data.add(n.getArrivalLocation());
            md.addRow(data);
        }
        tbl.setModel(md);
    }
    
    public void list() {
        // Nếu mà có dữ liệu hiện tại thì dùng lun , không có thì kéo db lên
        if (ltbs.getList() == null) {
            ltbs.list();
        }
        ArrayList<LoTrinhDTO> dsdto = ltbs.getList();
        ShowModel(model, dsdto);
    }

