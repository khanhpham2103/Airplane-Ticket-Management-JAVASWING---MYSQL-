    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String id = idText.getText();
        String Depart = DepartText.getText();
        String ArrVal = ArrvailText.getText();
        if (ltbs.getList() == null) {
            ltbs.list();
        }
        ArrayList<LoTrinhDTO> dslt = ltbs.getList();
        boolean check = false;
        for (LoTrinhDTO tmp : dslt) {
            if (tmp.getRouteID().equals(id)) {
                check = true;
                break;
            }
        }
        if (check) {
            JOptionPane.showMessageDialog(null, "Mã Bị Trùng");
            return;
        }
        LoTrinhDTO lotrinhTmp = new LoTrinhDTO(id, Depart, ArrVal);
        ltbs.add(lotrinhTmp);
        ShowModel(model, ltbs.getList());
        

    }//GEN-LAST:event_btnAddActionPerformed

