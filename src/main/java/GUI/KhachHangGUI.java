    private ArrayList<KhachHangDTO> Search(String SearchString, ArrayList<KhachHangDTO> data) {
        String DateFrom = TextYearFrom.getText();
        String DateTo = TextYearTo.getText();
        String filterString = SearchString;
        ArrayList<KhachHangDTO> dsarr = new ArrayList<KhachHangDTO>();
        ArrayList<KhachHangDTO> dsarr1 = new ArrayList<KhachHangDTO>();
        ArrayList<KhachHangDTO> dsarr2 = new ArrayList<KhachHangDTO>();


        if (getItemFilter == null) {
            System.out.println("search bth");
            for (KhachHangDTO i : data) {
                if (!SearchString.equals("")) {
                    if (i.getHoKH().toLowerCase().indexOf(SearchString.toLowerCase()) != -1 || i.getTenKH().toLowerCase().indexOf(SearchString.toLowerCase()) != -1) {

                        dsarr.add(i);
                    }
                }

            }
        } else {
            System.out.println("search nang cao");
            for (KhachHangDTO i : data) {
                if (i.getHoKH().toLowerCase().indexOf(SearchString.toLowerCase()) != -1 || i.getTenKH().toLowerCase().indexOf(SearchString.toLowerCase()) != -1) {
                    if (i.getGioiTinh().equals(getItemFilter) && getItemFilter != null) {
                        dsarr.add(i);
                    }
                }
            }
        }
        if (!TextYearFrom.getText().equals("")) {
            System.out.println("Dau Nam");

            int getYearInput = Integer.parseInt(TextYearFrom.getText());
            if (dsarr.size() > 0) {
                for (KhachHangDTO i : dsarr) {
                    String year = i.getBirth();
                    year = year.substring(0, year.indexOf('-'));
                    int getyear = Integer.parseInt(year);
                    if (getYearInput <= getyear) {
                        dsarr1.add(i);
                        System.out.println(getyear + "\n");
                    }
                }

                dsarr = dsarr1;
                
            } else {
                for (KhachHangDTO i : data) {
                    String year = i.getBirth();
                    year = year.substring(0, year.indexOf('-'));
                    int getyear = Integer.parseInt(year);
                    if (getYearInput <= getyear) {
                        dsarr.add(i);
                        System.out.println(getyear + "\n");
                    }
                }
            }

        }

        if (!TextYearTo.getText().equals("")) {
            System.out.println("Cuoi Nam");
            int getYearInput = Integer.parseInt(TextYearTo.getText());
            if (dsarr.size() > 0) {
                for (KhachHangDTO i : dsarr) {
                    String year = i.getBirth();
                    year = year.substring(0, year.indexOf('-'));
                    int getyear = Integer.parseInt(year);
                    if (getYearInput >= getyear) {
                        System.out.println(getyear + "\n");
                        dsarr2.add(i);
                    }
                }
                dsarr = dsarr2;
            } else {

                for (KhachHangDTO i : data) {
                    String year = i.getBirth();
                    year = year.substring(0, year.indexOf('-'));
                    int getyear = Integer.parseInt(year);
                    if (getYearInput >= getyear) {
                        System.out.println(getyear + "\n");
                        dsarr.add(i);
                    }
                }
            }

        }
        return dsarr;
    }

