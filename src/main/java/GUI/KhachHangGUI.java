    public void ShowModel(DefaultTableModel md, ArrayList<KhachHangDTO> dsdtp) {
        Vector data;
        model.setRowCount(0);
        for (KhachHangDTO n : dsdtp) {

            data = new Vector();
            data.add(n.getMaKH());
            data.add(n.getHoKH());
            data.add(n.getTenKH());
            data.add(n.getGioiTinh());
            data.add(n.getBirth());
            data.add(n.getAddress());
            data.add(n.getSDT());
            md.addRow(data);
        }
        tbl.setModel(md);
    }

